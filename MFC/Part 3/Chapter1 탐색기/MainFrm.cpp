BOOL CMainFrame::OnCreateClient(LPCREATESTRUCT lpcs, CCreateContext* pContext) 
{
	// TODO: Add your specialized code here and/or call the base class
	if (!wndSplitter.CreateStatic(this, 1, 2)) return false;

	if (!wndSplitter.CreateView(0, 0,RUNTIME_CLASS(CDirView), CSize(0,0), pContext)) return false;
	if (!wndSplitter.CreateView(0, 1,RUNTIME_CLASS(CExploerView), CSize(0,0), pContext)) return false;
	
	CRect r;
	GetClientRect(&r);

	int w1 = r.Width()/4;
	int w2 = r.Width()/4;

	wndSplitter.SetColumnInfo( 0, w1, 0 );
	wndSplitter.SetColumnInfo( 1, w2, 0 );

	wndSplitter.RecalcLayout();

	return true;
}
